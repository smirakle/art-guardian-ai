// Retry helper with exponential backoff for rate limits
async function fetchWithRetry(url: string, options: any, maxRetries = 3): Promise<Response> {
  let lastError;
  
  for (let attempt = 0; attempt < maxRetries; attempt++) {
    try {
      const response = await fetch(url, options);
      
      // If rate limited, wait and retry
      if (response.status === 429) {
        const retryAfter = response.headers.get('retry-after');
        const waitTime = retryAfter ? parseInt(retryAfter) * 1000 : Math.pow(2, attempt) * 1000;
        
        console.log(`Rate limited. Retrying in ${waitTime}ms (attempt ${attempt + 1}/${maxRetries})`);
        await new Promise(resolve => setTimeout(resolve, waitTime));
        continue;
      }
      
      return response;
    } catch (error) {
      lastError = error;
      if (attempt < maxRetries - 1) {
        const waitTime = Math.pow(2, attempt) * 1000;
        console.log(`Request failed. Retrying in ${waitTime}ms (attempt ${attempt + 1}/${maxRetries})`);
        await new Promise(resolve => setTimeout(resolve, waitTime));
      }
    }
  }
  
  throw lastError || new Error('Max retries exceeded');
}

// AI Training Pattern Detection using OpenAI Vision API
export async function detectAITrainingPatterns(imageUrl: string, artworkId: string): Promise<SearchResult[]> {
  const apiKey = Deno.env.get('OPENAI_API_KEY');
  
  if (!apiKey) {
    console.log('OpenAI API key not found for AI training detection');
    return [];
  }

  const results: SearchResult[] = [];

  try {
    console.log('Analyzing image for AI training patterns...');
    
    // First, generate a detailed description for fingerprinting
    const fingerprintResponse = await fetchWithRetry('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: 'You are an expert at analyzing images for unique identifying features that could be used to detect unauthorized use in AI training datasets. Focus on distinctive visual elements, composition, style, colors, and any unique characteristics.'
          },
          {
            role: 'user',
            content: [
              {
                type: 'text',
                text: 'Create a detailed fingerprint of this image, focusing on unique visual elements that would help identify it if it appeared in an AI training dataset. Include specific details about style, composition, colors, subjects, and any distinctive features.'
              },
              {
                type: 'image_url',
                image_url: { url: imageUrl }
              }
            ]
          }
        ],
        max_tokens: 500
      })
    });

    if (!fingerprintResponse.ok) {
      const errorText = await fingerprintResponse.text();
      console.error(`Failed to generate image fingerprint: ${fingerprintResponse.status} - ${errorText}`);
      return results;
    }

    const fingerprintData = await fingerprintResponse.json();
    const imageFingerprint = fingerprintData.choices[0]?.message?.content || '';
    
    // Analyze for specific AI training indicators
    const analysisResponse = await fetchWithRetry('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: 'You are an AI expert specializing in detecting signs that an image may have been used in AI training. Look for artifacts, inconsistencies, or patterns that suggest AI involvement in creation or processing.'
          },
          {
            role: 'user',
            content: [
              {
                type: 'text',
                text: 'Analyze this image for signs it may have been: 1) Used in AI training datasets, 2) Generated by AI, 3) Processed by AI tools, or 4) Contains AI artifacts. Provide a confidence score (0-100) and explain your reasoning.'
              },
              {
                type: 'image_url',
                image_url: { url: imageUrl }
              }
            ]
          }
        ],
        max_tokens: 400
      })
    });

    if (analysisResponse.ok) {
      const analysisData = await analysisResponse.json();
      const analysis = analysisData.choices[0]?.message?.content || '';
      
      // Extract confidence score from analysis
      const confidenceMatch = analysis.match(/confidence[:\s]*(\d+)/i);
      const confidence = confidenceMatch ? parseInt(confidenceMatch[1]) : 
                       analysis.toLowerCase().includes('likely') ? 75 :
                       analysis.toLowerCase().includes('possible') ? 60 :
                       analysis.toLowerCase().includes('unlikely') ? 30 : 50;

      // Check for AI training indicators
      const aiIndicators = [
        'training', 'dataset', 'synthetic', 'generated', 'artificial',
        'algorithm', 'model', 'neural', 'deep learning', 'artifacts'
      ];

      const foundIndicators = aiIndicators.filter(indicator => 
        analysis.toLowerCase().includes(indicator)
      );

      if (confidence > 60 || foundIndicators.length > 2) {
        results.push({
          platform: 'AI Training Detection',
          url: imageUrl,
          title: `Potential AI Training Usage Detected`,
          confidence: Math.min(confidence, 95),
          domain: 'ai-training-analysis',
          thumbnail: imageUrl,
          snippet: `Analysis suggests possible AI involvement. Indicators: ${foundIndicators.join(', ')}`
        });
      }
    }

    // Search for similar patterns in known AI datasets
    const datasetSearchResponse = await searchKnownAIDatasets(imageFingerprint, imageUrl);
    results.push(...datasetSearchResponse);

    console.log(`AI training detection completed: ${results.length} potential matches found`);
    return results;

  } catch (error) {
    console.error('AI training pattern detection error:', error);
    return results;
  }
}

